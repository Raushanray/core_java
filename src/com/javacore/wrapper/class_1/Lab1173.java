package com.javacore.wrapper.class_1;

public class Lab1173 {
	public static void main(String[] args) {
		System.out.println(Character.toLowerCase('A'));
		System.out.println(Character.toLowerCase('a'));
		System.out.println(Character.toLowerCase('3'));
		System.out.println(Character.toLowerCase(66));
		System.out.println(Character.toUpperCase('a'));
		System.out.println(Character.toUpperCase('A'));
		System.out.println(Character.toUpperCase('3'));
		System.out.println(Character.toUpperCase(98));
	}
}
