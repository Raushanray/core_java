package com.javacore.wrapper.class_1;

public class Lab1170 {
	public static void main(String[] args) {
		System.out.println(Character.isLowerCase('a'));
		System.out.println(Character.isLowerCase('A'));
		System.out.println(Character.isUpperCase('B'));
		System.out.println(Character.isUpperCase('b'));
		System.out.println(Character.isDigit('9'));
		System.out.println(Character.isDigit('q'));
		System.out.println(Character.isLetter('j'));
		System.out.println(Character.isLetter('9'));
		System.out.println(Character.isLetterOrDigit('9'));
		System.out.println(Character.isLetterOrDigit('J'));
		System.out.println(Character.isLetterOrDigit('$'));
	}
}
