package edu.javacore.switch_statement_326;

public class Lab355 {
	public static void main(String[] args) {
		int x = 0;
		switch (x) {
		case 0: {
			int a = 10;
			System.out.println("A1: " + a);
			break;
		}
		case 1: {
			 a = 90;
			System.out.println("A2: " + a);
			break;
		}
		}
	}
}
